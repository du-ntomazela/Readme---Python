def matriz0(matriz1, matriz2):
    for x in range(len(matriz1)):
        for y in range(len(matriz1[x])):
            if matriz1[x][y] == "#":
                matriz1[x][y] = "0"
    for x in range(len(matriz2)):
        for y in range(len(matriz2[x])):
            if matriz2[x][y] == "#":
                matriz2[x][y] = "0"
    print_matriz(matriz1, matriz2)

def print_matriz(matriz1, matriz2):
    for x in range(len(matriz1)):
        linha = ''
        for y in matriz1[x]:
            linha += y
        for y in matriz2[x]:
            linha += y
        print(linha)

